package impl;

public class Aula {
    private int numero;
    private int capacidad;

    public Aula(int numero, int capacidad) {
        this.numero = numero;
        this.capacidad = capacidad;
    }

    public int getNumero() {
        return this.numero;
    }

    public int getCapacidad() {
        return this.capacidad;
    }
}
